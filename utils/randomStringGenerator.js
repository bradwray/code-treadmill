export function randomTitle() {
  return titleCase(randomName() + " the " + randomAdj() + " " + randomJob());
}
export function randomAdj() {
  return adjectives[Math.floor(Math.random() * adjectives.length)];
}

export function randomJob() {
  return jobs[Math.floor(Math.random() * jobs.length)];
}

export function randomName() {
  return names[Math.floor(Math.random() * names.length)];
}

export function randomAnimals() {
  return animals[Math.floor(Math.random() * animals.length)];
}

export function randomVerb() {
  return verbs[Math.floor(Math.random() * verbs.length)];
}

function titleCase(string) {
  var sentence = string.toLowerCase().split(" ");
  for (var i = 0; i < sentence.length; i++) {
    sentence[i] =
      sentence[i].substring(0, 1).toUpperCase() + sentence[i].slice(1);
  }
  return sentence.join(" ");
}

export function randomSentence() {
  let adverb = adverbs[Math.floor(Math.random() * adverbs.length)];
  return (
    randomName() +
    " " +
    adverb +
    " " +
    randomVerb() +
    (Math.random() < 0.5 ? "ed" : "s") +
    " the " +
    randomAdj() +
    " " +
    randomAnimals() +
    "."
  );
}

